@st.cache_resource

def load_nlp():
    try:
        return stanza.Pipeline(
            lang='ru',
            processors='tokenize,pos,lemma,depparse',
            use_gpu=False
        )
    except:
        stanza.download('ru')
        return stanza.Pipeline(
            lang='ru',
            processors='tokenize,pos,lemma,depparse',
            use_gpu=False
        )

nlp = load_nlp()

